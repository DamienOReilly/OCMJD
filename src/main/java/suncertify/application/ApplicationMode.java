package suncertify.application;

/**
 * Enum to distinguish application launch modes.
 *
 * @author Damien O'Reilly
 */
public enum ApplicationMode {

    /**
     * The server mode.
     */
    SERVER,

    /**
     * The standalone mode.
     */
    ALONE,

    /**
     * The network client mode.
     */
    NETWORK

}